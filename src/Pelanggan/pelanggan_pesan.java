    private void tampilkanData(String kategori) {
    // 1. Definisikan kolom JTable Anda
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Menu");
    model.addColumn("Nama Menu");
    model.addColumn("Kategori");
    model.addColumn("Harga");
    model.addColumn("Stok");
    
    try {
        // 2. Buat koneksi ke database (sesuaikan dengan kelas koneksi Anda)
        Connection conn = koneksi.getConnection(); 
        
        // 3. Buat Query SQL dengan parameter WHERE
        String query = "SELECT * FROM menu WHERE kategori = ?";
        PreparedStatement ps = conn.prepareStatement(query);
        ps.setString(1, kategori); // Mengisi tanda tanya (?) dengan parameter kategori
        
        ResultSet rs = ps.executeQuery();
        
        // 4. Looping hasil query dan masukkan ke dalam model tabel
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_menu"),
                rs.getString("nama_menu"),
                rs.getString("kategori"),
                rs.getInt("harga"),
                rs.getInt("stok")
            });
        }
        
        // 5. Set model baru ke JTable Anda (misal namanya tabelMenu)
        jTable1.setModel(model);
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal mengambil data: " + e.getMessage());
    }
}
    private void hitungTotal() {
    int totalSemua = 0;
    int jumlahBaris = jTable2.getRowCount();
    
    for (int i = 0; i < jumlahBaris; i++) {
        int harga = Integer.parseInt(jTable2.getValueAt(i, 2).toString());
        int qty = Integer.parseInt(jTable2.getValueAt(i, 3).toString());
        
        // Menjumlahkan total keseluruhan (Harga Satuan * Qty)
        totalSemua += (harga * qty);
    }
    
    // Set hasil akhir ke label total transaksi di bawah tabel preview
    jLabel6.setText("Total: Rp " + totalSemua);
}

