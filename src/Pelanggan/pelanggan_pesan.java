    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int barisDipilih = jTable1.getSelectedRow();
    if (barisDipilih == -1) {
        JOptionPane.showMessageDialog(this, "Silakan pilih menu terlebih dahulu di tabel kiri!");
        return;
    }
    
    // 2. Validasi & Ambil nilai Qty dari JTextField txtQty
    int qtyInput = 0;
    try {
        String qtyTeks = txtQty.getValue().toString().trim();
        if (qtyTeks.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan jumlah terlebih dahulu!");
            return;
        }
        qtyInput = Integer.parseInt(qtyTeks);
        
        // Validasi jika user memasukkan angka 0 atau minus
        if (qtyInput <= 0) {
            JOptionPane.showMessageDialog(this, "Jumlah harus lebih dari 0!");
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka!");
        return;
    }
    
    // 3. Ambil data dari tabel kiri
    String idMenu = jTable1.getValueAt(barisDipilih, 0).toString();
    String namaMenu = jTable1.getValueAt(barisDipilih, 1).toString();
    int hargaSatuan = Integer.parseInt(jTable1.getValueAt(barisDipilih, 3).toString());
    
    // 4. Hitung Subtotal untuk item ini (Harga x Qty Input)
    int subTotalItem = hargaSatuan * qtyInput;
    
    // 5. Cek apakah menu ini sudah ada di tabel kanan (keranjang preview)
    boolean sudahAda = false;
    int indeksBarisAda = -1;
    
    for (int i = 0; i < jTable2.getRowCount(); i++) {
        if (jTable2.getValueAt(i, 0).toString().equals(idMenu)) {
            sudahAda = true;
            indeksBarisAda = i;
            break;
        }
    }
    
    // 6. Masukkan atau update data di tabel kanan
    if (sudahAda) {
        // Jika sudah ada, akumulasikan Qty lama dengan Qty baru
        int qtyLama = Integer.parseInt(jTable2.getValueAt(indeksBarisAda, 3).toString());
        int qtyTotalBaru = qtyLama + qtyInput;
        
        // Update kolom Qty (indeks 3)
        jTable2.setValueAt(qtyTotalBaru, indeksBarisAda, 3);
    } else {
        // Jika belum ada, masukkan data baru (ID, Nama, Harga Satuan, Qty Input)
        modelKeranjang.addRow(new Object[]{idMenu, namaMenu, hargaSatuan, qtyInput});
    }
    
    // 7. Hitung ulang total keseluruhan di bawah
    hitungTotal();
    
    // 8. RESET form Qty menjadi kosong kembali setelah sukses input
    txtQty.setValue("");
    }//GEN-LAST:event_jButton1ActionPerformed

